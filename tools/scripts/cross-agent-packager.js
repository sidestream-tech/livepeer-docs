#!/usr/bin/env node
/**
 * @script            cross-agent-packager
 * @category          generator
 * @purpose           governance:agent-governance
 * @scope             tools/scripts, ai-tools/ai-skills/catalog, ai-tools/agent-packs
 * @owner             docs
 * @needs             R-R27, R-R30
 * @purpose-statement Cross-agent packager — bundles audit reports and repo state into agent-consumable packages
 * @pipeline          manual — not yet in pipeline
 * @usage             node tools/scripts/cross-agent-packager.js [flags]
 */

const fs = require('fs');
const path = require('path');

const STAGE_ID = 'cross-agent-packager';
const REPO_ROOT = process.cwd();
const DEFAULT_OUTPUT_DIR = 'ai-tools/agent-packs';
const DEFAULT_CATALOG_PATH = 'ai-tools/ai-skills/catalog/skill-catalog.json';
const DEFAULT_MANIFEST_PATH = 'ai-tools/ai-skills/catalog/execution-manifest.json';
const VALID_PACKS = new Set(['codex', 'cursor', 'claude', 'windsurf', 'all']);

function toPosix(value) {
  return String(value || '').split(path.sep).join('/');
}

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function parseArgs(argv) {
  const out = {
    agentPack: 'all',
    outputDir: DEFAULT_OUTPUT_DIR,
    catalogPath: DEFAULT_CATALOG_PATH,
    manifestPath: DEFAULT_MANIFEST_PATH
  };

  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];

    if (token === '--agent-pack') {
      out.agentPack = String(argv[i + 1] || out.agentPack).trim() || out.agentPack;
      i += 1;
      continue;
    }
    if (token.startsWith('--agent-pack=')) {
      out.agentPack = token.slice('--agent-pack='.length).trim() || out.agentPack;
      continue;
    }
    if (token === '--output-dir') {
      out.outputDir = String(argv[i + 1] || out.outputDir).trim() || out.outputDir;
      i += 1;
      continue;
    }
    if (token.startsWith('--output-dir=')) {
      out.outputDir = token.slice('--output-dir='.length).trim() || out.outputDir;
      continue;
    }
    if (token === '--catalog') {
      out.catalogPath = String(argv[i + 1] || out.catalogPath).trim() || out.catalogPath;
      i += 1;
      continue;
    }
    if (token.startsWith('--catalog=')) {
      out.catalogPath = token.slice('--catalog='.length).trim() || out.catalogPath;
      continue;
    }
    if (token === '--manifest') {
      out.manifestPath = String(argv[i + 1] || out.manifestPath).trim() || out.manifestPath;
      i += 1;
      continue;
    }
    if (token.startsWith('--manifest=')) {
      out.manifestPath = token.slice('--manifest='.length).trim() || out.manifestPath;
      continue;
    }

    throw new Error(`Unknown argument: ${token}`);
  }

  if (!VALID_PACKS.has(out.agentPack)) {
    throw new Error(`Invalid --agent-pack value: ${out.agentPack}`);
  }

  return out;
}

function readJson(repoPath) {
  return JSON.parse(fs.readFileSync(path.join(REPO_ROOT, repoPath), 'utf8'));
}

function mdEscape(value) {
  return String(value || '').replace(/\|/g, '\\|').replace(/\n/g, ' ');
}

function pickPipelineSkills(catalog, manifest) {
  const skillMap = new Map((catalog.skills || []).map((skill) => [skill.id, skill]));
  const pipeline = Array.isArray(manifest.pipeline) ? manifest.pipeline : [];

  return pipeline
    .slice()
    .sort((a, b) => Number(a.run_order || 0) - Number(b.run_order || 0))
    .map((entry) => ({
      ...entry,
      skill: skillMap.get(entry.id) || null
    }))
    .filter((entry) => entry.skill);
}

function buildTopReadme(pipelineSkills) {
  const lines = [];
  lines.push('# Agent Packs');
  lines.push('');
  lines.push('This folder contains generated packs for running the same docs audit pipeline across supported coding agents.');
  lines.push('');
  lines.push('## Pipeline Stages');
  lines.push('');
  lines.push('| Order | Skill ID | Goal |');
  lines.push('|---:|---|---|');

  pipelineSkills.forEach((entry) => {
    lines.push(`| ${entry.run_order} | \`${mdEscape(entry.id)}\` | ${mdEscape(entry.skill.goal)} |`);
  });

  lines.push('');
  lines.push('Generated by: `node tools/scripts/cross-agent-packager.js --agent-pack all`');
  lines.push('');
  return `${lines.join('\n')}\n`;
}

function writeCodexPack(baseDirAbs, pipelineSkills, catalog, manifest) {
  const packDir = path.join(baseDirAbs, 'codex');
  ensureDir(packDir);

  const payload = {
    generated_at: new Date().toISOString(),
    source_catalog: DEFAULT_CATALOG_PATH,
    source_manifest: DEFAULT_MANIFEST_PATH,
    pipeline: pipelineSkills.map((entry) => ({
      run_order: entry.run_order,
      id: entry.id,
      goal: entry.skill.goal,
      commands: entry.skill.commands,
      outputs: entry.skill.outputs,
      severity_model: entry.skill.severity_model,
      autofix_mode: entry.skill.autofix_mode
    })),
    catalog_version: catalog.version,
    manifest_version: manifest.version
  };

  const manifestPath = path.join(packDir, 'skills-manifest.json');
  fs.writeFileSync(manifestPath, `${JSON.stringify(payload, null, 2)}\n`);

  return [toPosix(path.relative(REPO_ROOT, manifestPath))];
}

function buildRulePackMarkdown(agentName, pipelineSkills) {
  const lines = [];
  lines.push(`# ${agentName} Repo Audit Pack`);
  lines.push('');
  lines.push('Use these stages in order for a static-first docs infrastructure audit pipeline.');
  lines.push('');

  pipelineSkills.forEach((entry) => {
    lines.push(`## ${entry.run_order}. ${entry.id}`);
    lines.push('');
    lines.push(`- Goal: ${entry.skill.goal}`);
    lines.push(`- Severity Model: ${entry.skill.severity_model}`);
    lines.push(`- Autofix Mode: ${entry.skill.autofix_mode}`);
    lines.push('- Commands:');
    (entry.skill.commands || []).forEach((command) => {
      lines.push(`  - \`${command.run}\``);
    });
    lines.push('- Outputs:');
    (entry.skill.outputs || []).forEach((output) => {
      lines.push(`  - \`${output}\``);
    });
    lines.push('');
  });

  lines.push('Run source: `node tools/scripts/cross-agent-packager.js --agent-pack all`');
  lines.push('');
  return `${lines.join('\n')}\n`;
}

function writeCursorPack(baseDirAbs, pipelineSkills) {
  const packDir = path.join(baseDirAbs, 'cursor');
  ensureDir(packDir);

  const rulesPath = path.join(packDir, 'rules.md');
  fs.writeFileSync(rulesPath, buildRulePackMarkdown('Cursor', pipelineSkills));

  return [toPosix(path.relative(REPO_ROOT, rulesPath))];
}

function writeClaudePack(baseDirAbs, pipelineSkills) {
  const packDir = path.join(baseDirAbs, 'claude');
  ensureDir(packDir);

  const claudePath = path.join(packDir, 'CLAUDE.md');
  fs.writeFileSync(claudePath, buildRulePackMarkdown('Claude Code', pipelineSkills));

  return [toPosix(path.relative(REPO_ROOT, claudePath))];
}

function writeWindsurfPack(baseDirAbs, pipelineSkills) {
  const packDir = path.join(baseDirAbs, 'windsurf');
  ensureDir(packDir);

  const rulesPath = path.join(packDir, 'rules.md');
  fs.writeFileSync(rulesPath, buildRulePackMarkdown('Windsurf', pipelineSkills));

  return [toPosix(path.relative(REPO_ROOT, rulesPath))];
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const catalog = readJson(args.catalogPath);
  const manifest = readJson(args.manifestPath);
  const pipelineSkills = pickPipelineSkills(catalog, manifest);

  const outputDirAbs = path.resolve(REPO_ROOT, args.outputDir);
  ensureDir(outputDirAbs);

  const generated = [];

  if (args.agentPack === 'all' || args.agentPack === 'codex') {
    generated.push(...writeCodexPack(outputDirAbs, pipelineSkills, catalog, manifest));
  }
  if (args.agentPack === 'all' || args.agentPack === 'cursor') {
    generated.push(...writeCursorPack(outputDirAbs, pipelineSkills));
  }
  if (args.agentPack === 'all' || args.agentPack === 'claude') {
    generated.push(...writeClaudePack(outputDirAbs, pipelineSkills));
  }
  if (args.agentPack === 'all' || args.agentPack === 'windsurf') {
    generated.push(...writeWindsurfPack(outputDirAbs, pipelineSkills));
  }

  const readmePath = path.join(outputDirAbs, 'README.md');
  fs.writeFileSync(readmePath, buildTopReadme(pipelineSkills));
  generated.push(toPosix(path.relative(REPO_ROOT, readmePath)));

  console.log(`✅ ${STAGE_ID} generated ${generated.length} file(s):`);
  generated.forEach((filePath) => console.log(`- ${filePath}`));
}

if (require.main === module) {
  try {
    main();
  } catch (error) {
    console.error(`❌ ${STAGE_ID} failed: ${error.message}`);
    process.exit(1);
  }
}
